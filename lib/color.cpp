color makeColor(u8 _r, u8 _g, u8 _b, u8 _a){
    assert(_r >= 0 && _r <= 255);
    assert(_g >= 0 && _g <= 255);
    assert(_b >= 0 && _b <= 255);
    assert(_a >= 0 && _a <= 255);

    color result;
    
    result.r = _r;
    result.g = _g;
    result.b = _b;
    result.a = _a;

    return result;
}

