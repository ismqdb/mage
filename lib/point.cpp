point makePoint(u8 _x, u8 _y, u8 _z, u8 _w){
    assert(_x >= 0 && _x <= 255);
    assert(_y >= 0 && _y <= 255);
    assert(_z >= 0 && _z <= 255);
    assert(_w >= 0 && _w <= 255);

    point result;
    
    result.x = _x;
    result.y = _y;
    result.z = _z;
    result.w = _w;

    return result;
}

