/* **************************************************************************************************** */

#include "../headers/vec.hpp"

/* **************************************************************************************************** */

mage::vec3::vec3(){
    x = 0;
    y = 0;
    z = 0;
}

mage::vec3::vec3(const real a, const real b, const real c){
    x = a;
    y = b;
    z = c;
}

/* **************************************************************************************************** */

void mage::vec3::invert(){
    x = -x;
    y = -y;
    z = -z;
}

